#include <iostream>
using namespace std;
int main(){
    float a;
    cin >> a;
    cout << "The diameter of circle is equal to " << 2 * a << endl;
    cout << "The circumference of circle is equal to " << 2 * 3.14 * a << endl;
    cout << "The area of circle is equal to " << 3.14 * a * a << endl;
    return 0;
}